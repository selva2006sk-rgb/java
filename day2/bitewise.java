package day2;

public class bitewise
{
    public static void main(String[]args)
    {
        int a = 3;
        int b = 3;
        System.out.println(a&b); //for and operator
        System.out.println(a|b); // for or operator
        System.out.println(a^b); // for nor operator
        System.out.println(a<<b);//right shift
        System.out.println(a>>b); //left shift
    }
}
